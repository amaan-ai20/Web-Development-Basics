var name = "John Doe";

let age = 30;

const country = "USA";

console.log(name);
console.log(age);
console.log(country);

// var is deprecated in modern JavaScript, it's recommended to use let and const for variable declarations.


age = 31; // This is allowed with let

country = "Canada"; // This will throw an error because country is a constant

console.log(name);
console.log(age);
console.log(country);
